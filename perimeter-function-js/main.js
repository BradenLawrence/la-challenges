const perimeter = (width, height) => {
  return (width + height) * 2
}

console.log("Rectangle with width of 8 and height of 4:")
console.log(perimeter(8, 4))

console.log("Rectangle with width of 320 and height of 7:")
console.log(perimeter(320, 7))

console.log("Rectangle with width of 45.2 and height of 76.5:")
console.log(perimeter(45.2, 76.5))
